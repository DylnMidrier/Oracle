function inPoly(lon, lat, poly) {
  let inside = false;
  for (let i = 0, j = poly.length - 1; i < poly.length; j = i++) {
    const xi = poly[i][0], yi = poly[i][1], xj = poly[j][0], yj = poly[j][1];
    if ((yi > lat) !== (yj > lat) && lon < (xj - xi) * (lat - yi) / (yj - yi) + xi) inside = !inside;
  }
  return inside;
}

let cachedDots = null;
export function landDots() {
  if (cachedDots) return cachedDots;
  const dots = [];
  const step = 1.5;
  for (let lat = -56; lat <= 84; lat += step) {
    const lonStep = step / Math.max(0.25, Math.cos(lat * Math.PI / 180));
    for (let lon = -180; lon <= 180; lon += lonStep) {
      const jx = Math.sin(lat * 12.9898 + lon * 78.233) * 0.8;
      const jy = Math.sin(lat * 39.346 + lon * 11.135) * 0.8;
      const lo = lon + jx, la = lat + jy;
      let land = false;
      for (let p = 0; p < CONTINENTS.length; p++) {
        if (inPoly(lo, la, CONTINENTS[p])) { land = true; break; }
      }
      if (land) dots.push({ la: (la * Math.PI) / 180, lo: (lo * Math.PI) / 180 });
    }
  }
  cachedDots = dots;
  return dots;
}

// Projection orthographique inclinée (même technique que la maquette).
export function projectGlobe(la, lo, rot, R, T = 0.32) {
  const x = Math.cos(la) * Math.sin(lo + rot);
  const y = Math.sin(la);
  const z = Math.cos(la) * Math.cos(lo + rot);
  const y2 = y * Math.cos(T) - z * Math.sin(T);
  const z2 = y * Math.sin(T) + z * Math.cos(T);
  return { x: x * R, y: -y2 * R, z: z2 };
}

// Petit décalage déterministe pour éviter que les signaux d'une même région se superposent pile.
export function jitterCoords(region, seed) {
  const base = REGION_COORDS[region] || REGION_COORDS.Mondial;
  const h = Math.abs(hash(seed));
  const dLat = ((h % 700) / 100) - 3.5;
  const dLon = (((h >> 8) % 700) / 100) - 3.5;
  return [base[0] + dLat, base[1] + dLon];
}

function hash(str) {
  let h = 0;
  for (let i = 0; i < str.length; i++) h = (h * 31 + str.charCodeAt(i)) | 0;
  return h;
}
